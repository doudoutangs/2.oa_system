package com.bank.framework.web.controller;

import java.beans.PropertyEditorSupport;
import java.util.Date;
import java.util.List;
import org.springframework.web.bind.WebDataBinder;
import org.springframework.web.bind.annotation.InitBinder;
import com.github.pagehelper.PageHelper;
import com.github.pagehelper.PageInfo;
import com.bank.common.utils.DateUtils;
import com.bank.common.utils.StringUtils;
import com.bank.common.utils.security.ShiroUtils;
import com.bank.common.utils.sql.SqlUtil;
import com.bank.framework.web.domain.AjaxResult;
import com.bank.framework.web.domain.AjaxResult.Type;
import com.bank.framework.web.page.PageDomain;
import com.bank.framework.web.page.TableDataInfo;
import com.bank.framework.web.page.TableSupport;
import com.bank.project.system.user.domain.User;

